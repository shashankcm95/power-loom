#!/usr/bin/env node

// verify-plan-spawn.js — H.7.23 — pre-approval verification aggregator helper.
//
// IMPORTANT: this script does NOT spawn agents. Per H.7.23 plan code-reviewer
// FLAG #3, agent spawning happens in the skill body where Claude invokes the
// Agent tool twice in parallel. This script ONLY aggregates the resulting
// findings into a "Pre-Approval Verification" markdown block and appends it
// to the plan file.
//
// Closes drift-note 40 (codifies the parallel-spawn pre-approval verification
// pattern that caught real bugs in H.7.22 + H.7.23 plans).
//
// Usage:
//   node verify-plan-spawn.js <plan-path> <architect-findings-path> <code-reviewer-findings-path>
//
// Reads the two findings files (markdown), merges into a structured
// "Pre-Approval Verification" section, appends to plan file. The skill
// procedure handles all the hard parts (spawn, prompt design, fix-loop).

'use strict';

const fs = require('fs');
const path = require('path');

const PRE_APPROVAL_HEADER = '## Pre-Approval Verification';

function usage() {
  const prog = path.basename(process.argv[1] || 'verify-plan-spawn.js');
  console.error(`Usage: ${prog} <plan-path> <architect-findings-path> <code-reviewer-findings-path>`);
  console.error('');
  console.error('Reads architect + code-reviewer markdown findings; appends');
  console.error('aggregated "## Pre-Approval Verification" section to plan-path.');
  console.error('');
  console.error('Per H.7.23 design: this script does NOT spawn agents — that');
  console.error('happens in the skills/verify-plan/SKILL.md procedure (Claude');
  console.error('invokes Agent tool twice in parallel). This script aggregates only.');
  process.exit(1);
}

function readFile(p, label) {
  try {
    return fs.readFileSync(p, 'utf8');
  } catch (err) {
    console.error(`ERROR: failed to read ${label} (${p}): ${err.message}`);
    process.exit(2);
  }
}

function buildSection(architectFindings, codeReviewerFindings) {
  const ts = new Date().toISOString();
  return [
    '',
    PRE_APPROVAL_HEADER,
    '',
    `> Generated by \`/verify-plan\` at ${ts}. Per drift-note 40 (H.7.23), this`,
    '> section is required on HETS-routed plans before ExitPlanMode. Parallel',
    '> architect + code-reviewer spawn ran on this plan; findings merged below.',
    '',
    '### Architect verification',
    '',
    architectFindings.trim(),
    '',
    '### Code-reviewer verification',
    '',
    codeReviewerFindings.trim(),
    '',
    '### Verdict & next steps',
    '',
    '> Set verdict + apply any fixes here. If both reviewers returned PASS:',
    '> proceed to `ExitPlanMode`. If either returned NEEDS-REVISION: address',
    '> findings inline above (mark each as **Fixed** or **Acknowledged**),',
    '> optionally re-run `/verify-plan` for a clean pass.',
    '',
    'Status: PENDING (set to READY / NEEDS-REVISION / BLOCKED)',
    '',
  ].join('\n');
}

function appendSection(planPath, section) {
  const existing = readFile(planPath, 'plan');
  // If a Pre-Approval Verification section already exists, replace it
  // (idempotent re-run support — `/verify-plan` may be invoked multiple times).
  const re = new RegExp(`\\n## Pre-Approval Verification[\\s\\S]*?(?=\\n## |$)`, 'g');
  let updated;
  if (re.test(existing)) {
    updated = existing.replace(re, '\n' + section.trim() + '\n');
  } else {
    // Insert before "## Open design choices" if present, else at end.
    const insertBeforeRe = /\n## Open design choices/;
    if (insertBeforeRe.test(existing)) {
      updated = existing.replace(insertBeforeRe, '\n' + section.trim() + '\n\n## Open design choices');
    } else {
      updated = existing + '\n' + section.trim() + '\n';
    }
  }
  fs.writeFileSync(planPath, updated);
}

function main() {
  const args = process.argv.slice(2);
  if (args.length !== 3) usage();
  const [planPath, architectPath, codeReviewerPath] = args;

  if (!fs.existsSync(planPath)) {
    console.error(`ERROR: plan file not found: ${planPath}`);
    process.exit(2);
  }

  const architectFindings = readFile(architectPath, 'architect-findings');
  const codeReviewerFindings = readFile(codeReviewerPath, 'code-reviewer-findings');

  const section = buildSection(architectFindings, codeReviewerFindings);
  appendSection(planPath, section);

  console.log(`Appended "${PRE_APPROVAL_HEADER}" section to ${planPath}`);
  console.log('Review findings, set verdict, apply fixes, then ExitPlanMode.');
}

if (require.main === module) main();

